    private void delete_itemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_itemActionPerformed
        int response = JOptionPane.showConfirmDialog(
                this,
                "Seguro de Eliminar?",
                "Confirm",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (response == JOptionPane.OK_OPTION) {
            DefaultTreeModel m
                    = (DefaultTreeModel) jt_miembros.getModel();
            m.removeNodeFromParent(
                    nodo_seleccionado);
            m.reload();
        }
    }//GEN-LAST:event_delete_itemActionPerformed

    private void jt_CEOMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jt_CEOMouseClicked
        if (evt.isMetaDown()) {
            //seleccionar un nodo con click derecho
            int row = jt_CEO.getClosestRowForLocation(
                    evt.getX(), evt.getY());
            jt_CEO.setSelectionRow(row);
            Object v1
                    = jt_CEO.getSelectionPath().
                    getLastPathComponent();
            nodo_seleccionado = (DefaultMutableTreeNode) v1;
            if (nodo_seleccionado.getUserObject() instanceof Decano) {
                persona_seleccionada
                        = (Decano) nodo_seleccionado.
                        getUserObject();
                CRUD_CEO.show(evt.getComponent(),
                        evt.getX(), evt.getY());
            }else if(nodo_seleccionado.getUserObject() instanceof Docente){
                persona2_seleccionada
                        = (Docente) nodo_seleccionado.
                        getUserObject();
                CRUD_CEO.show(evt.getComponent(),
                        evt.getX(), evt.getY());
            }else if(nodo_seleccionado.getUserObject() instanceof Alumno){
                persona1_seleccionada
                        = (Alumno) nodo_seleccionado.
                        getUserObject();
                CRUD_CEO.show(evt.getComponent(),
                        evt.getX(), evt.getY());
            }

        }
    }//GEN-LAST:event_jt_CEOMouseClicked

