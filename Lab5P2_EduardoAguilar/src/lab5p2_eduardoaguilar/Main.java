    private void jButton7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton7MouseClicked
        String grupo ="";
        String coop = "";
        String participacion ="";
        String nota="";
        Object general =generalSpinner.getValue();
        
        
        if(bien1.isSelected()){
            grupo="Bien";
        }else if(regular1.isSelected()){
            grupo="Regular";
        }else if(mejorable1.isSelected()){
            grupo="Mejorable";
        }
        
        if(bien2.isSelected()){
            coop="Bien";
        }else if(regular2.isSelected()){
            coop="Regular";
        }else if(mejorable2.isSelected()){
            coop="Mejorable";
        }
        
        if(baja.isSelected()){
            participacion="Baja";
        }else if(moderada.isSelected()){
            participacion="Moderada";
        }else if(excelente.isSelected()){
            participacion="Excelente";
        }
        
        if(aprobado.isSelected()){
            nota="Aprobado";
        }else if(reprobado.isSelected()){
            nota="Reprobado";
        }
        Evaluaciones c = new Evaluaciones(grupo,coop,participacion,nota,general);
        evaluation.add(c);
        
        
        String[] nuevo = {
            c.getEquipo(),
            c.getCooperacion(),
            c.getParticipacion(),
            c.getNota(),
            String.valueOf(c.getGeneral())
        };
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        modelo.addRow(nuevo);
        tabla.setModel(modelo);
        
        for(int i = 0 ;i<estudiantes.size();i++){
            if(nombre_model.equals(estudiantes.get(i).getNombre())){
                estudiantes_evaluados.add(estudiantes.get(i));
            }
        }
        
        JD_evaluacion.setVisible(false);
        
        
        
        
    }//GEN-LAST:event_jButton7MouseClicked

